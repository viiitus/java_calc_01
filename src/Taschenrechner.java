public class Taschenrechner {

	public int addieren(int a, int b) {
		int ergebnis = a + b;
		return ergebnis;
	}

	public int substrahieren(int a, int b) {
		int ergebnis = a - b;
		return ergebnis;
	}

	public double multiplizieren(int a, int b) {
		double ergebnis = a * b;
		return ergebnis;
	}

	public double dividieren(double a, double b) {
		double ergebnis = a / b;
		return ergebnis;
	}

}
